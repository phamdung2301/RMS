import jakarta.persistence.*;
import lombok.*;
import java.time.LocalDateTime;

@Entity
@Table(name = "audit_logs")
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class AuditLog {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "user_id")
    private User user;

    @Column(name = "user_name")
    private String userName; // To keep username if user deleted or for fast access

    @Column(nullable = false)
    private String action; // e.g. LOGIN_SUCCESS, PASSWORD_CHANGED, DELETE_USER, APPROVE_PAYROLL

    @Column(name = "object_type")
    private String objectType; // e.g. User, Order, Employee

    @Column(name = "object_id")
    private String objectId;

    @Column(columnDefinition = "TEXT")
    private String description;

    @Column(name = "ip_address")
    private String ipAddress;

    @Column(name = "branch_id", length = 36)
    private String branchId;

    @Column(name = "created_at", nullable = false)
    private LocalDateTime createdAt = LocalDateTime.now();
}
