import jakarta.persistence.*;
import lombok.*;

@Entity
@Table(name = "order_details")
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class OrderDetail {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "order_id", nullable = false)
    private Order order;

    @ManyToOne(fetch = FetchType.EAGER)
    @JoinColumn(name = "variant_id", nullable = false)
    private ProductVariant variant;

    @Column(nullable = false)
    private Integer quantity;

    @Column(nullable = false)
    private String status = "PENDING"; // PENDING, COOKING, READY, SERVED, CANCELLED

    private String notes; // e.g. "ít cay", "không hành"

    @Column(nullable = false)
    private Double price; // Price at purchase time

    @Column(name = "is_deducted", nullable = false)
    @Builder.Default
    private boolean isDeducted = false;
}

